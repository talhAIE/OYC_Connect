-- -----------------------------------------------------------------------------
-- 1. Table to avoid sending the same notification twice for the same (date, slot, time).
--    Deduplication is by actual prayer time so changing a prayer time and testing again
--    will send a new notification when the new time comes.
-- -----------------------------------------------------------------------------
create table if not exists public.prayer_notification_sent (
  id bigint generated by default as identity primary key,
  notify_date date not null,
  slot text not null,  -- e.g. 'fajr_30', 'fajr_on', 'dhuhr_30', 'jummah_30', ...
  prayer_time time not null,  -- the actual adhan time we notified for (so changing time = new notification)
  sent_at timestamptz not null default now(),
  unique (notify_date, slot, prayer_time)
);

-- If table already existed without prayer_time, add column and fix constraint
do $$
begin
  if exists (
    select 1 from information_schema.tables
    where table_schema = 'public' and table_name = 'prayer_notification_sent'
  ) and not exists (
    select 1 from information_schema.columns
    where table_schema = 'public' and table_name = 'prayer_notification_sent' and column_name = 'prayer_time'
  ) then
    alter table public.prayer_notification_sent add column prayer_time time;
    update public.prayer_notification_sent set prayer_time = (sent_at at time zone 'utc')::time where prayer_time is null;
    alter table public.prayer_notification_sent alter column prayer_time set not null;
    alter table public.prayer_notification_sent drop constraint if exists prayer_notification_sent_notify_date_slot_key;
    alter table public.prayer_notification_sent add constraint prayer_notification_sent_notify_date_slot_prayer_time_key unique (notify_date, slot, prayer_time);
  end if;
end;
$$;

